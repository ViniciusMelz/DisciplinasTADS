    public void setIndice(int indice) {
        this.indice = indice;
        
        SemestreLetivo d = DisciplinaTADS.listaSL.get(indice);
        String ano = d.getAno() + "";
        jftxtAno.setText(ano);
        jftxtDataInicial.setText(new SimpleDateFormat("dd/MM/yyyy").format(d.getDataInicio()));
        jftxtDataFinal.setText(new SimpleDateFormat("dd/MM/yyyy").format(d.getDataFim()));
        
        switch (d.getSemestre()) {
            case 1 -> jcbSemestre.setSelectedIndex(1);
            case 2 -> jcbSemestre.setSelectedIndex(2);
            default -> throw new AssertionError();
        }
    }
