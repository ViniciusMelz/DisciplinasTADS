    private void jbtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSalvarActionPerformed
        try{
            if(jftxtAno.getText().isEmpty() == true){
                JOptionPane.showMessageDialog(null, "Preencha o Campo Ano!");
                jftxtAno.requestFocus();
            }else if(Integer.parseInt(jftxtAno.getText()) < 2022){
                JOptionPane.showMessageDialog(null, "Preencha o Campo Ano Com Um Valor Valido!");
                jftxtAno.requestFocus();
            }else if(jcbSemestre.getSelectedIndex() == 0){
                JOptionPane.showMessageDialog(null, "Selecione um Semestre Valido!");
                jcbSemestre.requestFocus();
            }else if(jftxtDataInicial.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Preencha o Campo Data Inicial!");
                jftxtDataInicial.requestFocus();
            }else if(jftxtDataFinal.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Preencha o Campo Data Final!");
                jftxtDataFinal.requestFocus();
            }else if(new SimpleDateFormat("dd/MM/yyyy").parse(jftxtDataInicial.getText()).after(new SimpleDateFormat("dd/MM/yyyy").parse(jftxtDataFinal.getText())) == true){
                JOptionPane.showMessageDialog(null, "Preencha o Campo Data Final Com uma Data Valida!");
                jftxtDataFinal.requestFocus();
            }else{
                int ano = Integer.parseInt(jftxtAno.getText());
                int semestre = jcbSemestre.getSelectedIndex();
                SemestreLetivo sl = new SemestreLetivo(ano, 
                                           semestre,
                                           new SimpleDateFormat("dd/MM/yyyy").parse(jftxtDataInicial.getText()), 
                                           new SimpleDateFormat("dd/MM/yyyy").parse(jftxtDataFinal.getText()));
                if(indice == -1){
                    DisciplinaTADS.listaSL.add(sl);
                }else{
                    DisciplinaTADS.listaSL.alterar(sl, indice);
                }
                dispose();
                TelaSemestresLetivos telaSemestresLetivos = new TelaSemestresLetivos();
                telaSemestresLetivos.setVisible(true);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jbtnSalvarActionPerformed

